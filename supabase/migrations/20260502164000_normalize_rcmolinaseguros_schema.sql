create or replace function public.usuarios_gerar_codigo_login(p_email text)
returns table(codigo text, expira_em timestamptz)
language plpgsql
security definer
set search_path = public, "RCMOLINASEGUROS", extensions
as $$
declare
  v_email text := lower(trim(p_email));
  v_user_id uuid;
  v_codigo text;
  v_now timestamptz := timezone('utc', now());
begin
  select id
  into v_user_id
  from "RCMOLINASEGUROS"."USUARIOS"
  where lower(email) = v_email
  limit 1;

  if v_user_id is null then
    return;
  end if;

  if exists (
    select 1
    from "RCMOLINASEGUROS"."CODIGOS_LOGIN"
    where email = v_email
      and created_at >= (v_now - interval '60 seconds')
  ) then
    raise exception 'RATE_LIMIT';
  end if;

  v_codigo := lpad(((random() * 999999)::int)::text, 6, '0');

  insert into "RCMOLINASEGUROS"."CODIGOS_LOGIN" (
    usuario_id,
    email,
    codigo_hash,
    expires_at
  )
  values (
    v_user_id,
    v_email,
    crypt(v_codigo, gen_salt('bf')),
    v_now + interval '10 minutes'
  );

  return query
  select v_codigo, v_now + interval '10 minutes';
end;
$$;

create or replace function public.usuarios_verificar_codigo_login(
  p_email text,
  p_codigo text
) returns setof "RCMOLINASEGUROS"."USUARIOS"
language plpgsql
security definer
set search_path = public, "RCMOLINASEGUROS", extensions
as $$
declare
  v_email text := lower(trim(p_email));
  v_codigo_row "RCMOLINASEGUROS"."CODIGOS_LOGIN"%rowtype;
begin
  select *
  into v_codigo_row
  from "RCMOLINASEGUROS"."CODIGOS_LOGIN"
  where email = v_email
    and used_at is null
    and expires_at > timezone('utc', now())
    and attempts < 5
  order by created_at desc
  limit 1;

  if not found then
    return;
  end if;

  if v_codigo_row.codigo_hash <> crypt(p_codigo, v_codigo_row.codigo_hash) then
    update "RCMOLINASEGUROS"."CODIGOS_LOGIN"
    set attempts = attempts + 1
    where id = v_codigo_row.id;
    return;
  end if;

  update "RCMOLINASEGUROS"."CODIGOS_LOGIN"
  set used_at = timezone('utc', now()),
      attempts = attempts + 1
  where id = v_codigo_row.id;

  return query
  select *
  from "RCMOLINASEGUROS"."USUARIOS"
  where id = v_codigo_row.usuario_id;
end;
$$;

create or replace function public.usuarios_resetar_senha_com_codigo(
  p_email text,
  p_codigo text,
  p_nova_senha text
) returns jsonb
language plpgsql
security definer
set search_path = public, "RCMOLINASEGUROS", extensions
as $$
declare
  v_email text := lower(trim(p_email));
  v_codigo_row "RCMOLINASEGUROS"."CODIGOS_LOGIN"%rowtype;
  v_remaining integer;
begin
  select *
  into v_codigo_row
  from "RCMOLINASEGUROS"."CODIGOS_LOGIN"
  where email = v_email
    and used_at is null
  order by created_at desc
  limit 1;

  if not found then
    return jsonb_build_object('status', 'INVALID', 'message', 'Codigo nao encontrado.', 'remaining', 0);
  end if;

  if v_codigo_row.expires_at <= timezone('utc', now()) then
    return jsonb_build_object('status', 'EXPIRED', 'message', 'Codigo expirado.');
  end if;

  if v_codigo_row.attempts >= 5 then
    return jsonb_build_object('status', 'BLOCKED', 'message', 'Codigo bloqueado.');
  end if;

  if v_codigo_row.codigo_hash <> crypt(p_codigo, v_codigo_row.codigo_hash) then
    update "RCMOLINASEGUROS"."CODIGOS_LOGIN"
    set attempts = attempts + 1
    where id = v_codigo_row.id
    returning greatest(0, 5 - attempts) into v_remaining;

    return jsonb_build_object(
      'status', 'INVALID',
      'message', 'Codigo incorreto.',
      'remaining', greatest(coalesce(v_remaining, 0), 0)
    );
  end if;

  update "RCMOLINASEGUROS"."USUARIOS"
  set senha_hash = crypt(p_nova_senha, gen_salt('bf')),
      updated_at = timezone('utc', now())
  where id = v_codigo_row.usuario_id;

  update "RCMOLINASEGUROS"."CODIGOS_LOGIN"
  set used_at = timezone('utc', now()),
      attempts = attempts + 1
  where id = v_codigo_row.id;

  return jsonb_build_object('status', 'SUCCESS', 'message', 'Senha atualizada com sucesso.');
end;
$$;

create or replace function public.admin_list_users()
returns setof "RCMOLINASEGUROS"."USUARIOS"
language plpgsql
security definer
set search_path = public, "RCMOLINASEGUROS", extensions
as $$
begin
  if auth.jwt() ->> 'email' != 'admin@rcmolina.com.br' then
    raise exception 'Acesso negado: você não tem permissão de administrador.';
  end if;

  return query
  select *
  from "RCMOLINASEGUROS"."USUARIOS"
  order by nome_completo;
end;
$$;

