  add column if not exists detalhes jsonb not null default '{}'::jsonb,
  add column if not exists created_at timestamptz not null default timezone('utc', now());

alter table "RCMOLINASEGUROS"."CODIGOS_LOGIN"
  add column if not exists attempts integer not null default 0,
  add column if not exists created_at timestamptz not null default timezone('utc', now());

create index if not exists usuarios_email_idx on "RCMOLINASEGUROS"."USUARIOS" (email);
create index if not exists usuarios_nome_idx on "RCMOLINASEGUROS"."USUARIOS" (nome_completo);
