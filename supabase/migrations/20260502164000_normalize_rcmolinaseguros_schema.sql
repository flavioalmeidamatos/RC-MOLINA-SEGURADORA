create or replace function public.usuarios_cadastrar(
  p_email text,
  p_senha text,
  p_nome_completo text,
  p_organizacao text default null,
  p_avatar_url text default null
) returns uuid
language plpgsql
security definer
set search_path = public, "RCMOLINASEGUROS", extensions
as $$
declare
  v_email text := lower(trim(p_email));
  v_user_id uuid;
begin
  if exists (
    select 1
    from "RCMOLINASEGUROS"."USUARIOS"
    where lower(email) = v_email
  ) then
    raise exception 'Este e-mail já está cadastrado.';
  end if;

  insert into "RCMOLINASEGUROS"."USUARIOS" (
    email,
    senha_hash,
    nome_completo,
    organizacao,
    avatar_url
  )
  values (
    v_email,
    crypt(p_senha, gen_salt('bf')),
    upper(trim(p_nome_completo)),
    nullif(trim(p_organizacao), ''),
    nullif(trim(p_avatar_url), '')
  )
  returning id into v_user_id;

  return v_user_id;
end;
$$;

