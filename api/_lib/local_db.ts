export const usuariosGerarCodigoLogin = async (email: string) => {
  await initLocalDatabase();
  const normalizedEmail = email.trim().toLowerCase();
  const user = await getPool().query(
    'select id from "RCMOLINASEGUROS"."USUARIOS" where lower(email) = $1 limit 1',
    [normalizedEmail],
  );

  if (!user.rows[0]) return null;

  const recent = await getPool().query(
    `select 1 from "RCMOLINASEGUROS"."CODIGOS_LOGIN"
     where email = $1 and created_at >= (timezone('utc', now()) - interval '60 seconds')
     limit 1`,
    [normalizedEmail],
  );

  if (recent.rowCount) {
    const error = new Error('RATE_LIMIT');
    error.name = 'RATE_LIMIT';
    throw error;
  }

  const codigo = crypto.randomInt(0, 1000000).toString().padStart(6, '0');
  const result = await getPool().query(
    `insert into "RCMOLINASEGUROS"."CODIGOS_LOGIN" (usuario_id, email, codigo_hash, expires_at)
     values ($1, $2, crypt($3, gen_salt('bf')), timezone('utc', now()) + interval '10 minutes')
     returning expires_at`,
    [user.rows[0].id, normalizedEmail, codigo],
  );

  return {
    codigo,
    expira_em: result.rows[0]?.expires_at,
  };
};

export const usuariosVerificarCodigoLogin = async (email: string, codigo: string) => {
  await initLocalDatabase();
  const normalizedEmail = email.trim().toLowerCase();
  const codeResult = await getPool().query(
    `select *
     from "RCMOLINASEGUROS"."CODIGOS_LOGIN"
     where email = $1
       and used_at is null
       and expires_at > timezone('utc', now())
       and attempts < 5
     order by created_at desc
     limit 1`,
    [normalizedEmail],
  );
  const row = codeResult.rows[0];

  if (!row) return null;

  const valid = await getPool().query('select $1 = crypt($2, $1) as valid', [row.codigo_hash, codigo]);

  if (!valid.rows[0]?.valid) {
    await getPool().query('update "RCMOLINASEGUROS"."CODIGOS_LOGIN" set attempts = attempts + 1 where id = $1', [
      row.id,
    ]);
    return null;
  }

  await getPool().query(
    `update "RCMOLINASEGUROS"."CODIGOS_LOGIN"
     set used_at = timezone('utc', now()), attempts = attempts + 1
     where id = $1`,
    [row.id],
  );

  return usuariosPerfil(row.usuario_id);
};

export const usuariosResetarSenhaComCodigo = async (email: string, codigo: string, novaSenha: string) => {
  await initLocalDatabase();
  const normalizedEmail = email.trim().toLowerCase();
  const codeResult = await getPool().query(
    `select *
     from "RCMOLINASEGUROS"."CODIGOS_LOGIN"
     where email = $1 and used_at is null
     order by created_at desc
     limit 1`,
    [normalizedEmail],
  );
  const row = codeResult.rows[0];

  if (!row) return { status: 'INVALID', message: 'Codigo nao encontrado.', remaining: 0 };
  if (new Date(row.expires_at).getTime() <= Date.now()) return { status: 'EXPIRED', message: 'Codigo expirado.' };
  if (row.attempts >= 5) return { status: 'BLOCKED', message: 'Codigo bloqueado.' };

  const valid = await getPool().query('select $1 = crypt($2, $1) as valid', [row.codigo_hash, codigo]);

  if (!valid.rows[0]?.valid) {
    const attempts = await getPool().query(
      `update "RCMOLINASEGUROS"."CODIGOS_LOGIN"
       set attempts = attempts + 1
       where id = $1
       returning attempts`,
      [row.id],
    );
    const remaining = Math.max(0, 5 - Number(attempts.rows[0]?.attempts || 5));
    return { status: 'INVALID', message: 'Codigo incorreto.', remaining };
  }

  await getPool().query(
    `update "RCMOLINASEGUROS"."USUARIOS"
     set senha_hash = crypt($1, gen_salt('bf')), updated_at = timezone('utc', now())
     where id = $2`,
    [novaSenha, row.usuario_id],
  );
  await getPool().query(
    `update "RCMOLINASEGUROS"."CODIGOS_LOGIN"
     set used_at = timezone('utc', now()), attempts = attempts + 1
     where id = $1`,
    [row.id],
  );
  await registrarAuditoria('RESET_SENHA', { email: normalizedEmail });

  return { status: 'SUCCESS', message: 'Senha atualizada com sucesso.' };
};

const authSecret = () => process.env.AUTH_SECRET || process.env.ADMIN_INITIAL_PASSWORD || 'rc-molina-dev-secret';

export const createAdminToken = (email: string) => {
  const payload = Buffer.from(
    JSON.stringify({
      email: email.trim().toLowerCase(),
      exp: Date.now() + 1000 * 60 * 60 * 8,
    }),
  ).toString('base64url');
  const signature = crypto.createHmac('sha256', authSecret()).update(payload).digest('base64url');
  return `${payload}.${signature}`;
};

export const verifyAdminToken = (token: string | undefined) => {
  if (!token) return false;
  const [payload, signature] = token.replace(/^Bearer\s+/i, '').split('.');
  if (!payload || !signature) return false;

  const expected = crypto.createHmac('sha256', authSecret()).update(payload).digest('base64url');
  if (!crypto.timingSafeEqual(Buffer.from(signature), Buffer.from(expected))) return false;

  const decoded = JSON.parse(Buffer.from(payload, 'base64url').toString('utf8')) as { email?: string; exp?: number };
  return decoded.email === (process.env.ADMIN_EMAIL || 'admin@rcmolina.com.br').toLowerCase() && Number(decoded.exp) > Date.now();
};

