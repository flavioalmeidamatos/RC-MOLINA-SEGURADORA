const rowToPerfil = (row: any): UsuarioPerfil => ({
  id: row.id,
  email: row.email,
  nome_completo: row.nome_completo,
  organizacao: row.organizacao,
  avatar_url: row.avatar_url,
  created_at: row.created_at?.toISOString?.() || row.created_at,
  updated_at: row.updated_at?.toISOString?.() || row.updated_at,
});

export const initLocalDatabase = async () => {
  if (!initPromise) {
    initPromise = (async () => {
      await getPool().query(schemaSql);
      await seedAdminUser();
    })();
  }

  return initPromise;
};

export const localDatabaseStatus = async () => {
  await initLocalDatabase();
  const result = await getPool().query(`
    select
      current_database() as database,
      current_user as "user",
      (
        select count(*)
        from information_schema.tables
        where table_schema = 'RCMOLINASEGUROS'
      )::int as tables
  `);

  return result.rows[0];
};

const seedAdminUser = async () => {
  const adminEmail = (process.env.ADMIN_EMAIL || 'admin@rcmolina.com.br').trim().toLowerCase();
  const adminPassword = process.env.ADMIN_INITIAL_PASSWORD || '';

  if (!adminPassword) return;

  const existing = await getPool().query('select 1 from "RCMOLINASEGUROS"."USUARIOS" where lower(email) = $1 limit 1', [
    adminEmail,
  ]);

  if (existing.rowCount) {
    await getPool().query(
      `update "RCMOLINASEGUROS"."USUARIOS"
       set senha_hash = crypt($2, gen_salt('bf')),
           nome_completo = coalesce(nullif(trim(nome_completo), ''), $3),
           organizacao = coalesce(nullif(trim(organizacao), ''), $4)
       where lower(email) = $1`,
      [adminEmail, adminPassword, 'ADMINISTRADOR RC MOLINA', 'RC MOLINA'],
    );
    return;
  }

  await getPool().query(
    `insert into "RCMOLINASEGUROS"."USUARIOS" (email, senha_hash, nome_completo, organizacao)
     values ($1, crypt($2, gen_salt('bf')), $3, $4)`,
    [adminEmail, adminPassword, 'ADMINISTRADOR RC MOLINA', 'RC MOLINA'],
  );
};

