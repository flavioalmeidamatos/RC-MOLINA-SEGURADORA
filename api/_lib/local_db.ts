const avatarExtensionByMime: Record<string, string> = {
  'image/jpeg': 'jpg',
  'image/png': 'png',
  'image/webp': 'webp',
  'image/gif': 'gif',
  'image/bmp': 'bmp',
  'image/svg+xml': 'svg',
};

export const saveAvatarDataUrl = async (dataUrl?: string | null, originalName = 'avatar') => {
  if (!dataUrl) return null;

  const match = dataUrl.match(/^data:(image\/[a-zA-Z0-9.+-]+);base64,(.+)$/);
  if (!match) return null;

  const [, mime, base64] = match;
  const extension =
    avatarExtensionByMime[mime] || path.extname(originalName).replace(/^\./, '').toLowerCase() || 'png';
  const buffer = Buffer.from(base64, 'base64');

  if (buffer.length > 5 * 1024 * 1024) {
    throw new Error('A imagem do avatar deve ter no maximo 5 MB.');
  }

  const uploadRoot = process.env.UPLOAD_DIR || path.join(process.cwd(), 'uploads');
  const avatarDir = path.join(uploadRoot, 'avatars');
  await fs.mkdir(avatarDir, { recursive: true });

  const filename = `${crypto.randomUUID()}.${extension}`;
  const finalPath = path.join(avatarDir, filename);
  await fs.writeFile(finalPath, buffer);

  return `/uploads/avatars/${filename}`;
};
